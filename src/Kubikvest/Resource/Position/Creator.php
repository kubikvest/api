namespace Kubikvest\Resource\Position;

use iMega\Formatter\Formatter;
use iMega\Formatter\FormatterException;
use Pimple\Container;

class Creator
{
    /**
     * @var Container
     */
    private $container;

    public function __construct(Container $container)
    {
        $this->container = $container;
    }

    /**
     * @return Model\Position
     */
    public function create()
    {
        $formatter = new Formatter(
            [
                PositionFormatType::setDefault('content', 0)
            ]
        );

        return $formatter->getValue('content', $this->container['request.content']);
    }
}
